# 输出hello python!!!
# 单行注释:
# 格式: # 对代码的描述文字
# 快捷键:ctrl+/   取消注释:ctrl+/ (除了Mac本)
# cmd+/ (mac本)
# 注意:如果打了注释的代码Python是不会执行注释的代码或者文字
# print("hello python!!!")


# 多行注释:
# 格式:"""说明的文字"""     或者'''说明的文字'''

"""
 打印一首诗
 古诗名字:春江花月夜
 作者:不详
"""

'''
 打印一首诗
 古诗名字:春江花月夜
 作者:不详
'''

"""
  总结:
      如果一句话可以解释清楚 可以使用单行注释
      如果需要多行文字描述  可以使用多行注释
      
   注释的作用:
       方便后期测试多个程序服务  害怕忘记
       快速脱身
       测试代码
"""